// Função utilitária para buscar ingredientes
export function searchIngredients(query: string, limit = 8): string[] {
    if (!query || query.length < 2) return [];

    const normalizedQuery = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');

    // Priorizar ingredientes que começam com a query
    const startsWithMatches: string[] = [];
    const containsMatches: string[] = [];

    for (const ingredient of BRAZILIAN_INGREDIENTS) {
        const normalizedIngredient = ingredient.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');

        if (normalizedIngredient.startsWith(normalizedQuery)) {
            startsWithMatches.push(ingredient);
        } else if (normalizedIngredient.includes(normalizedQuery)) {
            containsMatches.push(ingredient);
        }
    }

    return [...startsWithMatches, ...containsMatches].slice(0, limit);
}

